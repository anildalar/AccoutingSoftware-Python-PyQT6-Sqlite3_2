CREATE TABLE IF NOT EXISTS companies ( 
    id   INTEGER PRIMARY KEY,
    company_name TEXT NOT NULL COLLATE NOCASE,
    country TEXT NOT NULL COLLATE NOCASE,
    state TEXT NOT NULL COLLATE NOCASE,
    city TEXT NOT NULL COLLATE NOCASE,
    address TEXT NOT NULL COLLATE NOCASE,
    pincode TEXT NOT NULL COLLATE NOCASE,
    fy_start_month TEXT NOT NULL COLLATE NOCASE,
    fy_start_year TEXT NOT NULL COLLATE NOCASE,
    fy_end_month TEXT NOT NULL COLLATE NOCASE,
    fy_end_year TEXT NOT NULL COLLATE NOCASE
);
CREATE TABLE IF NOT EXISTS users (
    id   INTEGER PRIMARY KEY,
    username TEXT NOT NULL COLLATE NOCASE,
    password TEXT NOT NULL COLLATE NOCASE,
    role TEXT CHECK(role IN ('admin', 'accountant', 'operator'))
);
CREATE TABLE IF NOT EXISTS customers ( 
    id   INTEGER PRIMARY KEY,
    name TEXT NOT NULL COLLATE NOCASE,
    compId INTEGER
);
CREATE TABLE IF NOT EXISTS suppliers ( 
    id   INTEGER PRIMARY KEY,
    name TEXT NOT NULL COLLATE NOCASE,
    compId INTEGER
);
CREATE TABLE IF NOT EXISTS items ( 
    id   INTEGER PRIMARY KEY,
    name TEXT NOT NULL COLLATE NOCASE,
    compId INTEGER
);
